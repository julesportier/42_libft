#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*mem;

	if (
		nmemb * size <= 0
		|| nmemb > 2147483647 / size
	)
	{
		mem = malloc(1);
		ft_bzero(mem, 1);
		return (mem);
	}
	mem = malloc(nmemb * size);
	if (mem == NULL)
		return (NULL);
	ft_bzero(mem, nmemb * size);
	return (mem);
}
